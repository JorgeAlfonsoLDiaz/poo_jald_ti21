numero1 = input("Introduzca el primer número: ")  # Guarda en la variable "numero1" el valor de la entrada de datos del usuario.
numero2 = input("Introduzca el segundo número: ")  # Guarda en la variable "numero2" el valor de la entrada de datos del usuario.
numero3 = input("Introduzca el tercer número: ")  # Guarda en la variable "numero3" el valor de la entrada de datos del usuario.

resultado = int(numero1) + int(numero2) + int(numero3)  # Guarda en la variable "resultado" la suma de numero1, numero2 y numero3.

print(resultado)  # Imprime en pantalla el valor de la variable "resultado".